CREATE
DATABASE tech_store_manager;
USE
tech_store_manager;

-- PRIMARY KEY
CREATE TABLE products
(
    id             int auto_increment primary key,
    product_name   varchar(255),
    purchase_price decimal(10, 2),
    sale_price     decimal(10, 2),
    brand          varchar(50),
    img_address    varchar(255)
);

CREATE TABLE categories
(
    id            int auto_increment primary key,
    category_name varchar(100)
);

CREATE TABLE warehouses
(
    id      int auto_increment primary key,
    name    varchar(100),
    address varchar(255)
);

CREATE TABLE stores
(
    id      int auto_increment primary key,
    name    varchar(100),
    address varchar(255)
);

CREATE TABLE customers
(
    id           int auto_increment primary key,
    name         varchar(255),
    phone_number varchar(15)
);

-- FOREIGN KEY
-- PRODUCT
CREATE TABLE product_categories
(
    id_product  int,
    id_category int,
    foreign key (id_product) references products (id),
    foreign key (id_category) references categories (id)
);

-- WAREHOUSES
CREATE TABLE products_warehouse
(
    id_product   int,
    id_warehouse int,
    quantity     int,
    foreign key (id_warehouse) references warehouses (id),
    foreign key (id_product) references products (id)
);

CREATE TABLE import_warehouse
(
    id                  int auto_increment primary key,
    id_warehouse        int,
    name varchar(50),
    total               decimal(12,2),
    product_import_date datetime,
	status varchar(50),
    foreign key (id_warehouse) references warehouses (id)
);

CREATE TABLE import_warehouse_details
(
    id_import int,
    id_product int,
    quantity int,
    total decimal(12,2),
    foreign key (id_import) references import_warehouse (id),
    foreign key (id_product) references products (id)
);

-- STORES
CREATE TABLE store_financial
(
    id             int auto_increment primary key,
    id_store       int,
    date           date,
    turnover       decimal(12,2),
    capital        decimal(12, 2),
    profit         decimal(12, 2),
    foreign key (id_store) references stores (id)
);


CREATE TABLE products_store
(
    id_product int,
    id_store   int,
    quantity   int,
    foreign key (id_store) references stores (id),
    foreign key (id_product) references products (id)
);

CREATE TABLE import_store
(
    id            int auto_increment primary key,
    name varchar(255),
    id_store      int,
    id_warehouse int,
    total         decimal(10, 2),
    received_date datetime,
    status varchar(50),
    foreign key (id_warehouse) references warehouses (id),
    foreign key (id_store) references stores (id)
);

CREATE TABLE import_store_details
(
    id_import   int,
    id_product  int,
    quantity    int,
    total       decimal(10, 2),
    foreign key (id_import) references import_store (id),
    foreign key (id_product) references products (id)
);

-- PERSON
CREATE TABLE role
(
    id   int auto_increment primary key,
    role varchar(50)
);

CREATE TABLE employees
(
    id           int auto_increment primary key,
    first_name   varchar(50),
    last_name    varchar(50),
    gender       boolean,
    dob          date,
    email        varchar(255),
    phone_number varchar(15),
    address      varchar(255),
    hire_date    datetime default current_timestamp,
    salary       decimal(10, 2),
    id_role      int,
    id_store     int,
    id_warehouse int,
    foreign key (id_role) references role (id),
    foreign key (id_store) references stores (id),
    foreign key (id_warehouse) references warehouses (id)
);


CREATE TABLE accounts
(
    id        int auto_increment primary key,
    username  varchar(50),
    password  varchar(255),
    id_person int,
    foreign key (id_person) references employees (id)
);

-- ORDERS
CREATE TABLE receipts
(
    id            int auto_increment primary key,
    id_customer   int,
    id_store      int,
    id_cashier int,
    total         decimal(10, 2),
    profit        decimal(10, 2),
    purchase_date datetime,
    foreign key (id_cashier) references employees (id),
    foreign key (id_customer) references customers (id),
    foreign key (id_store) references stores (id)
);

CREATE TABLE products_receipt
(
    id_receipt  int,
    id_product  int,
    quantity    int,
    total_amount decimal(10, 2),
    profit      decimal(10, 2),
    foreign key (id_receipt) references receipts (id),
    foreign key (id_product) references products (id)
);

-- ENTERPRISE 
CREATE TABLE business_financial
(
    id             int auto_increment primary key,
    date           date,
    turnover       decimal(12,2),
    capital        decimal(12, 2),
    profit         decimal(12, 2)
);


